#!/usr/bin/env node
// R1 — Sentry event → regression test scaffold.
// Reads a Sentry event JSON (from `sentry-cli events show <id>` or pasted dump)
// and generates a tests/regression/INC-<date>-<slug>.test.ts skeleton.
// Operator reviews + fills in the reproducer.
//
// Usage:
//   node scripts/sentry-to-regression-test.mjs --event=event.json
//   node scripts/sentry-to-regression-test.mjs --stdin < event.json
//   echo '<event-json>' | node scripts/sentry-to-regression-test.mjs --stdin

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs'
import { resolve, dirname } from 'node:path'

const args = process.argv.slice(2).reduce((acc, a) => {
  const m = a.match(/^--([^=]+)(?:=(.*))?$/)
  if (m) acc[m[1]] = m[2] ?? true
  return acc
}, {})

let raw
if (args.event) {
  raw = readFileSync(args.event, 'utf8')
} else if (args.stdin) {
  raw = await new Promise(r => {
    let buf = ''
    process.stdin.on('data', c => (buf += c))
    process.stdin.on('end', () => r(buf))
  })
} else {
  console.error('Usage: --event=<file.json> OR --stdin')
  console.error('Sentry event JSON from `sentry-cli events show <id>`')
  process.exit(1)
}

let event
try {
  event = JSON.parse(raw)
} catch (e) {
  console.error('Failed to parse JSON:', e.message)
  process.exit(2)
}

// Extract key info from Sentry event
const eventId = event.event_id || event.id || 'unknown'
const date = (event.received || event.timestamp || new Date().toISOString()).slice(0, 10)
const message = event.message?.formatted || event.title || event.exception?.values?.[0]?.value || 'unknown'
const slug = String(message)
  .slice(0, 50)
  .toLowerCase()
  .replace(/[^a-z0-9]+/g, '-')
  .replace(/^-|-$/g, '')
  .replace(/--+/g, '-') || 'incident'

const fileName = `INC-${date}-${slug}.test.ts`
const filePath = resolve(import.meta.dirname, '../tests/regression', fileName)

if (existsSync(filePath)) {
  console.error(`File already exists: ${filePath}`)
  process.exit(3)
}

const exception = event.exception?.values?.[0]
const stack = exception?.stacktrace?.frames?.slice(-3).reverse().map(
  f => `  at ${f.function || '<anonymous>'} (${f.filename}:${f.lineno})`
).join('\n') || '<stack frames not available>'

const tags = Object.entries(event.tags || {}).map(([k, v]) => `${k}=${v}`).join(', ')
const breadcrumbs = (event.breadcrumbs?.values || []).slice(-5).map(
  b => `  • ${b.timestamp || ''} [${b.category}] ${b.message || JSON.stringify(b.data || {})}`
).join('\n') || '<no breadcrumbs>'

const userAction = event.request?.url || event.contexts?.runtime?.name || 'unknown'

const scaffold = `// Regression test for Sentry incident ${eventId}
// Reproduced from: ${event.received || event.timestamp || 'unknown'}
//
// Original error: ${message}
// Tags: ${tags}
// User action: ${userAction}
//
// Stack (top 3):
${stack.split('\n').map(l => `// ${l}`).join('\n')}
//
// Last breadcrumbs:
${breadcrumbs.split('\n').map(l => `// ${l}`).join('\n')}
//
// REPRODUCTION:
// 1. [TODO operator] Identify minimal trigger from above signals
// 2. Write test that fails on current main → fix → test passes
// 3. PR title: 'fix(regression): ${slug}'
//
// Generated by scripts/sentry-to-regression-test.mjs

import { describe, it, expect } from 'vitest'

describe('Regression INC-${date}-${slug}', () => {
  it.todo('reproduces: ${message.slice(0, 80)}')

  // Suggested approach based on Sentry signals:
  // - If exception involves DB → mock pool.query to return error type
  // - If exception involves Go fetch → mock fetch with status 500
  // - If exception involves user input → use the request.body from Sentry
  //   (event.request.data) as fixture
  //
  // After writing reproducer, replace 'it.todo' with 'it'.
})
`

mkdirSync(dirname(filePath), { recursive: true })
writeFileSync(filePath, scaffold)
console.log(`✓ Generated: ${filePath}`)
console.log(`  Event ID: ${eventId}`)
console.log(`  Date:     ${date}`)
console.log(`  Slug:     ${slug}`)
console.log(`\nNext step: write the reproducer in 'it.todo' block.`)
console.log(`Test runs in default scope (tests/regression/ included).`)
